# sort() method vs sorted() funtion

numbers = [4, 6, 2, 5, 7, 11]
numbers.sort()
print(numbers)

names = ["Afiq", "Mahathir", "Anwar Ibrahim", "Najib Razak", "Takumi Minamino", "Alice in Woderland", "Badarul", "Lim Kit Siang"]
print(names)
print(sorted(names))
print(names)

print(sorted(names, key = len))



 